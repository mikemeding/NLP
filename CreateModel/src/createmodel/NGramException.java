public class NGramException extends Exception{

	public NGramException() {
		super();
	}

	public NGramException(String message) {
		super(message);
	}

	public NGramException(String message, Throwable cause) {
		super(message, cause);
	}

	public NGramException(Throwable cause) {
		super(cause);
	}
	
}
